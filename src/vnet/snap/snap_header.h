#define included_snap_header_h

#include <vnet/vnet.h>

typedef union
{
  CLIB_PACKED (struct {
    /* OUI: organization unique identifier. */
    u8 oui[3];

    /* Per-OUI protocol. */
    u16 protocol;
  });

  u8 as_u8[5];
} snap_header_t;

